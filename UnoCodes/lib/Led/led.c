/////////////////////////////////////////////////INCLUDES/////////////////////////////////////////////////////////////
#include "avr/io.h"
#include "led.h"

/////////////////////////////////////////////////DEFINES/////////////////////////////////////////////////////////////

#define LEDPIN PB5
#define LEDPORT PORTB
#define LEDDDR DDRB

/////////////////////////////////////////////////FUNCTION PROTOTYPES/////////////////////////////////////////////////

void led_builtinInit(void)
{
    LEDDDR |= (1 << LEDPIN);
    led_builtinOff();
}
void led_builtinOn(void)
{

    LEDPORT |= (1 << LEDPIN);
}
void led_builtinOff(void)
{
    LEDPORT &= ~(1 << LEDPIN);
}
void led_builtinToggle(void)
{

    LEDPORT ^= (1 << LEDPIN);
}